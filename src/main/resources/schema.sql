CREATE TABLE IF NOT EXISTS INQUIRY (
INQUIRY_ID BIGINT AUTO_INCREMENT PRIMARY KEY,
USER_NO BIGINT,
CATEGORY VARCHAR(20) NOT NULL,
TITLE VARCHAR(200) NOT NULL,
CONTENT TEXT NOT NULL,
STATUS VARCHAR(30) NOT NULL DEFAULT 'UNREAD',
ANSWER TEXT,
ANSWERED_AT DATETIME,
REGDATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
MODDATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

CONSTRAINT FK_INQUIRY_USER FOREIGN KEY (USER_NO) REFERENCES USERS(USER_NO),
CONSTRAINT CK_INQUIRY_CATEGORY CHECK (CATEGORY IN ('분실물','습득물','서비스','기타')),
CONSTRAINT CK_INQUIRY_STATUS CHECK (STATUS IN ('UNREAD','CHECKING','ANSWERED'))
);

